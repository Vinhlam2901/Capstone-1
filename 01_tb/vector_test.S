.global _start
    .option norvc

_start:
    # LỆNH ASSEMBLY GỐC         # LỆNH THỰC TẾ (Compiler dịch)  # MÃ HEX MACHINE CODE
    # ===============================================================================
    # 1. TỰ TẠO DATA GIẢ
    li x6, 0x00000000           # addi x6, x0, 0                # 0x00000313
    li x28, 0x05050505          # lui x28, 0x05050              # 0x05050E37
                                # addi x28, x28, 0x505          # 0x505E0E13                
    sw x28, 0(x6)               # sw x28, 0(x6)                 # 0x01C32023
    sw x28, 4(x6)               # sw x28, 4(x6)                 # 0x01C32223
    # ===============================================================================
    # 2. CẤU HÌNH VECTOR (Xử lý 8 pixel)
    li x10, 8                   # addi x10, x0, 8               # 0x00800513
    li x11, 0x0000              # addi x11, x0, 0               # 0x00000593
    .word 0x0C057657            # vsetvli x12, x10, e8, m1      # 0x0C057657
    # ===============================================================================
    # 3. VECTOR LOAD (Lấy số 5 từ RAM lên v1)
    .word 0x02058087            # vle8.v v1, (x11)              # 0x02058087
    # ===============================================================================
    # 4. VECTOR ALU (Cộng 10)
    .word 0x021530D7            # vadd.vi v1, v1, 10 
    # ===============================================================================
    # 5. VECTOR STORE (Ghi kết quả ra RAM)
    li x11, 0x0010              # addi x11, x0, 16              # 0x01000593
    .word 0x020580A7            # vse8.v v1, (x11)              # 0x020580A7
done:
    j done                      # jal x0, 0                     # 0x0000006F